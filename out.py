# Tokenizer
tokenizer = Tokenizer()
tokenizer.fit_on_texts(kannada_corpus)
total_words = len(tokenizer.word_index) + 1

# Generate input sequences (n-gram sequences)
input_sequences = []
for line in kannada_corpus:
    token_list = tokenizer.texts_to_sequences([line])[0]
    for i in range(1, len(token_list)):
        n_gram_seq = token_list[:i+1]
        input_sequences.append(n_gram_seq)

# Pad sequences
max_seq_len = max([len(x) for x in input_sequences])
input_sequences = pad_sequences(input_sequences, maxlen=max_seq_len, padding='pre')

# Create features and labels
xs, labels = input_sequences[:,:-1], input_sequences[:,-1]
ys = tf.keras.utils.to_categorical(labels, num_classes=total_words)

# Model
model = Sequential()
model.add(Embedding(total_words, 64, input_length=max_seq_len - 1))
model.add(LSTM(100))
model.add(Dense(total_words, activation='softmax'))

model.compile(loss='categorical_crossentropy', optimizer='adam', metrics=['accuracy'])
model.fit(xs, ys, epochs=500, verbose=0)

# Prediction function
def predict_next_words(seed_text, num_predictions=10):
    token_list = tokenizer.texts_to_sequences([seed_text])[0]
    token_list = pad_sequences([token_list], maxlen=max_seq_len - 1, padding='pre')
    predicted_probs = model.predict(token_list, verbose=0)[0]

    top_indices = predicted_probs.argsort()[-num_predictions:][::-1]
    predicted_words = [tokenizer.index_word.get(i, '') for i in top_indices]

    return predicted_words

# API Route
@app.route('/predict', methods=['POST'])
def predict():
    data = request.get_json()
    input_text = data.get('text')
    num_predictions = data.get('num_predictions', 10)

    if not input_text:
        return jsonify({'error': 'No input text provided'}), 400

    predictions = predict_next_words(input_text, num_predictions)
    return jsonify({
        'input': input_text,
        'predictions': predictions
    })

